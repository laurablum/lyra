export function debug(msg: string) {
  // eslint-disable-next-line no-console
  console.debug(msg);
}

export function info(msg: string) {
  // eslint-disable-next-line no-console
  console.info(msg);
}

export function warn(msg: string) {
  // eslint-disable-next-line no-console
  console.warn(msg);
}

export function error(msg: string) {
  // eslint-disable-next-line no-console
  console.error(msg);
}
